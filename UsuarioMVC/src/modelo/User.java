package modelo;

public class User {

    // atributos de mi clase
    private  String name;
    private  String email;


    // constructor para inicializar mis variables  que son name y email
    // alt mas insert para generar el constructo mas rapido y lo mismo con los configurar y almacenar

    public User() {
        this.name = name;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}


